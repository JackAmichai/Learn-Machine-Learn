// ====== Derived helpers ======

export function getAllTags() {
  const set = new Set();
  RESOURCES.forEach((r) => r.tags.forEach((t) => set.add(t)));
  return Array.from(set).sort();
}

export function getResourcesByCategory(categoryId) {
  return RESOURCES.filter((r) => r.category === categoryId);
}

export function searchResources(query, { category, level, tag, freeOnly } = {}) {
  const q = (query || '').trim().toLowerCase();
  return RESOURCES.filter((r) => {
    if (category && r.category !== category) return false;
    if (level && r.level !== level) return false;
    if (tag && !r.tags.includes(tag)) return false;
    if (freeOnly && !r.free) return false;
    if (!q) return true;
    return (
      r.title.toLowerCase().includes(q) ||
      r.author.toLowerCase().includes(q) ||
      r.summary.toLowerCase().includes(q) ||
      r.tags.some((t) => t.toLowerCase().includes(q))
    );
  });
}
